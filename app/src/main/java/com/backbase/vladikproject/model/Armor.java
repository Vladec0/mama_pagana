package com.backbase.vladikproject.model;

public class Armor {

    private int id;
    private String name;
    private int width;
    private boolean hasEkran;

    public Armor(int id, String name, int width, boolean hasEkran){
        this.id = id;
        this.name = name;
        this.width = width;
        this.hasEkran = hasEkran;
    }

}
